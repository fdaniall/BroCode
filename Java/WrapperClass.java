
public class WrapperClass {

	public static void main(String[] args) {
		
		// Wrapper class = Provides a way to use primitive class data types as reference data type as reference data types 
		// 				   contain useful methods can be used with collections (ex.ArrayList)
		
		// Primitive	||	Wrapper
		// ---------	||  -------
		// boolean		||	Boolean
		// char			||  	Character
		// int			||	Integer
		// double		||	Double
		
		// Autoboxing	= The automatic conversion that the Java compiler makes between the primitive types 
		//				  and their corresponding object wrapper classes
		// Unboxing		= The reverse of autoboxing. Automatic conversion of wrapper class to primitive
		
		Boolean 	a 	= 	true;
		Character 	b 	= 	'@';
		Integer 	c 	= 	123;
		Double 		d 	= 	3.14;
		String 		e 	= 	"Alpha";
		
	}
}
